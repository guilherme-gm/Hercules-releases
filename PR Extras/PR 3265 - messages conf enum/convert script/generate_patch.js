/**
 *
 * @param {string} line
 */
function patchLine(line) {
	const targetMatches = line.matchAll(/(msg_[s|f]d)\((.*?),\s*(\d+)\s*\)/g);

	for (const match of targetMatches) {
		const cmd = match[1];
		const firstParam = match[2];
		const id = Number(match[3]);

		if (!idTable[id]) {
			throw new Error(`Failed to handle -- ${match[0]}`);
		}

		line = line.replace(match[0], `${cmd}_patched(${firstParam}, ${idTable[id]})`);
	}

	const headlessMatches = line.matchAll(/msg_txt\(\s*(\d+)\s*\)/g);

	for (const match of headlessMatches) {
		const id = Number(match[1]);

		if (!idTable[id]) {
			throw new Error(`Failed to handle -- ${match[0]}`);
		}

		line = line.replace(match[0], `msg_txt_patched(${idTable[id]})`);
	}

	return line;
}

function writeHeader(file) {
	patch.push(
		`--- a/${file} 2023-12-18 13:00:00.0 -0300`,
		`+++ b/${file} 2023-12-18 13:01:00.0 -0300`,
		'',
	);
}

function writePatch(lineNumber, fromLine, toLine) {
	patch.push(
		`@@ -${lineNumber},1 +${lineNumber},1 @@`,
		`-${fromLine}`,
		`+${toLine}`,
	);
}

for (const file of files) {
	const content = fs.readFileSync(file).toString().split('\n');
	let headerWritten = false;

	for (let lineNum = 0; lineNum < content.length; lineNum++) {
		const line = content[lineNum];
		const newLine = patchLine(line);;

		if (newLine !== line) {
			if (!headerWritten) {
				writeHeader(file);
				headerWritten = true;
			}

			writePatch(lineNum, line, newLine);
		}
	}

	if (headerWritten)
		patch.push('');
}

fs.writeFileSync('generated_patch.patch', patch.join('\n'));
